package route;

public class RouteData {
	public final String login;
	public final String password;
	public final String peer;
	public final String service;
	public final String vdn;
	
	public RouteData(final String login, final String password, final String peer, final String service, final String vdn) {
		super();
		this.login = login;
		this.password = password;
		this.peer = peer;
		this.service = service;
		this.vdn = vdn;
	}
	
	
}
